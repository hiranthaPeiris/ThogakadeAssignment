    private void loadItemCodes(){
        try {
            ArrayList <String> itemCodes=ItemController.getAllItemCodes();
            for (int i = 0; i < itemCodes.size(); i++){
		comboNewItemCode.addItem(itemCodes.get(i));
                ComboGrnItemCode.addItem(itemCodes.get(i));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void loadCustIds(){
        try {
            ArrayList <Customer> custList=CustomerController.getAllCustomers();
            for(int i=0;i<custList.size();i++){
                Customer customer=custList.get(i);
                comboCustId.addItem(customer.getId());
                comboDelCustID.addItem(customer.getId());
                comboUpCustId.addItem(customer.getId());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
