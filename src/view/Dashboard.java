    private void bttGRNSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttGRNSaveActionPerformed
        if("".equals(txtGRNNewStock.getText())){
            JOptionPane.showMessageDialog(Grn, "Please enter new stock data!!", "Feedback", JOptionPane.OK_OPTION);
        }else{
            try {
                DefaultTableModel dtm=(DefaultTableModel)GRNTable.getModel();
                ArrayList <Item> ItemList=new ArrayList<>();
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    String code=(String) ComboGrnItemCode.getSelectedItem();
                    String desc=txtGRNDescription.getText();
                    double unitP=Double.parseDouble(txtGRNUnitPrice1.getText());
                    int qtyonHand=Integer.parseInt(txtGRNQtyOnHand.getText());
                    int newStock=Integer.parseInt(txtGRNNewStock.getText());
                    Item item=new Item(code, desc, unitP, qtyonHand, newStock);
                    ItemList.add(item);
                }
                boolean state=ItemController.updateReceiveStock(ItemList);
                if(state){
                    JOptionPane.showMessageDialog(Grn, "Successfully Updated Items", "FeedBack", JOptionPane.OK_CANCEL_OPTION);
                    dtm.getDataVector().removeAllElements();
                    dtm.fireTableDataChanged();
                    txtGRNNewStock.setText("");
                }else{
                    JOptionPane.showMessageDialog(Grn, "Update failed, please Try again!!", "Feedback", JOptionPane.OK_CANCEL_OPTION);
            }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bttGRNSaveActionPerformed

